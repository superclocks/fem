      
	SUBROUTINE MEMBRANE_MASS(VN,VXY,X,Y,SJ,AMM,WXY,VMAE)
	IMPLICIT  REAL* 8 (A-H, O-Z)
      COMMON/COM1/MND, NUMEL, NUMPT, MBAND, NMATI
      COMMON/COM2/NF, NFSTR, MSOLV, MPROB, MTYPE, NVA
      COMMON/COM3/MND2 , NUMPT2
      COMMON/ELEM/NODE, INTX, INTY
      DIMENSION  VN(9),VMM(12,12),VNN(3,12),AMM(12,12),VMAE(4),VXY(4,3)
	DENS=VMAE(3)
	TH=VMAE(4)

      VNN(:,:)=0.0
	DO 10 I=1,4
	DO 10 J=1,3
      VNN(J,(I-1)*3+J)=VN(I)
10    CONTINUE
      AMM(:,:)=0.0
      DO 11 I=1,12
	DO 11 J=1,12
	DO 11 K=1,3
      AMM(I,J)=AMM(I,J)+VNN(K,I)*VNN(K,J)*SJ*WXY*DENS*TH 
11    CONTINUE
      RETURN
	END