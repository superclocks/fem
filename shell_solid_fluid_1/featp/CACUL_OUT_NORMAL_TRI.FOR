	SUBROUTINE CACUL_OUT_NORMAL_TRI(FXY,FN,NUM,MR,VX)
      IMPLICIT   REAL * 8(A-H,O-Z)
	COMMON/COM/NNN
      DIMENSION  FXY(8,3),FN(3),XY(4,3),A12(3),A23(3),MR(8),MRR(4),
     $          VX(3,3),TCOS(3,3),VXY(4,3)
      
	VX(:,:)=0.0
	SELECT CASE(NUM)
	CASE(1)
      VXY(1,:)=FXY(2,:)
      VXY(2,:)=FXY(1,:)
	VXY(3,:)=FXY(4,:)
	VXY(4,:)=FXY(3,:)
	MRR(1)=MR(2)
	MRR(2)=MR(1)
	MRR(3)=MR(4)
	MRR(4)=MR(3)
	CASE(2)
      VXY(1,:)=FXY(1,:)
      VXY(2,:)=FXY(2,:)
	VXY(3,:)=FXY(6,:)
	VXY(4,:)=FXY(5,:)
	MRR(1)=MR(1)
	MRR(2)=MR(2)
	MRR(3)=MR(6)
	MRR(4)=MR(5)
	CASE(3)
      VXY(1,:)=FXY(2,:)
      VXY(2,:)=FXY(3,:)
	VXY(3,:)=FXY(7,:)
	VXY(4,:)=FXY(6,:)
	MRR(1)=MR(2)
	MRR(2)=MR(3)
	MRR(3)=MR(7)
	MRR(4)=MR(6)
	CASE(4)
      VXY(1,:)=FXY(3,:)
      VXY(2,:)=FXY(4,:)
	VXY(3,:)=FXY(8,:)
	VXY(4,:)=FXY(7,:)
	MRR(1)=MR(3)
	MRR(2)=MR(4)
	MRR(3)=MR(8)
	MRR(4)=MR(7)
	CASE(5)
      VXY(1,:)=FXY(4,:)
      VXY(2,:)=FXY(1,:)
	VXY(3,:)=FXY(5,:)
	VXY(4,:)=FXY(8,:)
	MRR(1)=MR(4)
	MRR(2)=MR(1)
	MRR(3)=MR(5)
	MRR(4)=MR(8)
	CASE(6)
      VXY(1,:)=FXY(5,:)
      VXY(2,:)=FXY(6,:)
	VXY(3,:)=FXY(7,:)
	VXY(4,:)=FXY(8,:)
	MRR(1)=MR(5)
	MRR(2)=MR(6)
	MRR(3)=MR(7)
	MRR(4)=MR(8)
	ENDSELECT

	IF(MRR(3).NE.MRR(4))THEN
      WRITE(*,*)"三角形耦合面坐标错误"
      STOP
	ENDIF
	MR(1:3)=MRR(1:3)

	VX(:,:)=VXY(1:3,:)
       
	CALL TRI_TRANS(VX,TCOS)
      FN(:)=TCOS(:,3)
	RETURN
	END