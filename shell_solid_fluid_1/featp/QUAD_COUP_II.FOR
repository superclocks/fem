      SUBROUTINE  QUAD_COUP_II(VXY,SN,QQ)
      IMPLICIT REAL*8 (A-H,O-Z)
      DIMENSION VXY(4,3),QQ(12,4),TC(12,4),SN(3)

	
	QQ(:,:)=0.0
      INTX=2
	INTY=2
	DO 10 I=1,INTX
	DO 10 J=1,INTY
	CALL GAUSS_INTEGRATION (INTX,INTY,I,J,X,Y,WXY)
      CALL QUAD_COUP_I(TC,VXY,SN,X,Y)
      QQ(:,:)=QQ(:,:)+TC(:,:)
10    CONTINUE


 	RETURN
	END